Enhances Wickerbottom with durability-free item usage and periodic recovery, and gives Walter extra magic-book synergy.

Current features:
- Wickerbottom does not consume finite uses on owned items while using them.
- Wickerbottom does not consume fueled durability on owned items while using them.
- Wickerbottom does not lose armor durability on owned armor while using it.
- Wickerbottom restores 15 hunger, 15 sanity, and 15 health every 60 seconds.
- Wickerbottom also gets the base game's life injector max-health penalty recovery every 60 seconds.
- Walter can use Wickerbottom's magic books without consuming their uses.
- Walter's slingshot ammo is not consumed while he is carrying at least one magic book.
- While Walter is carrying at least one magic book, his items also stop consuming uses, fueled durability, and armor durability.
- While Walter is carrying at least one magic book, he also restores 15 hunger, 15 sanity, and 15 health every 60 seconds and gets life injector-style max-health penalty recovery.

This mod stays focused on Wickerbottom and Walter magic utility upgrades.
